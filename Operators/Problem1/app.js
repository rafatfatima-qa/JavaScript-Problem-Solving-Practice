var x = 5, y = 10, z = 15;

document.write("x = 5, y = 10, z = 15"); //display variable's values on browser page
document.write('<br> <br>'); //newline

document.write("This is the output of x + y: ", x + y);
document.write('<br> <br>');

document.write("This is the output of y - x: ", y - x); 
document.write('<br> <br>');

document.write("This is the output of x * y: ", x * y);
document.write('<br> <br>');

document.write("This is the output of y / x: ", y / x); 
document.write('<br> <br>');

document.write("This is the output of x % 2: ", x % 2); 
document.write('<br> <br>');

/*If used postfix, with operator after operand (for example, x++),
the increment operator increments and returns the value before incrementing. */
document.write("This is the output of x++: ", x++); 
document.write('<br> <br>');

/*If used postfix, with operator after operand (for example, x--),
the decrement operator decrements and returns the value before decrementing. */
document.write("This is the output of x--: ", x--); 
document.write('<br> <br>');


